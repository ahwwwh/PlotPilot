"""章节正文「反八股 / 控水分」约束块 — 供主工作流与 DAG 等共用。

解决模型常见恶习：「不是…而是…」纠正式排比、破折号叠比喻、
同一段感受换三种说法、为凑字数重复描写同一致震撼等。
"""
from __future__ import annotations

from typing import Optional


def build_prose_discipline_block(
    *,
    beat_mode: bool = False,
    beat_target_words: Optional[int] = None,
) -> str:
    """组装注入 system 的戒律段落（中文）。"""
    lines = [
        "━━━ 行文戒律（反八股 / 控水分）━━━",
        "",
        "**纠正式对照（全章禁止）**：禁止在同一句或紧挨两句里用「否定甲 + 肯定乙」的说教对照（含「不是…而是…」「从来不是…」「要的不是…是…」「并非…乃…」及同义变体）。想转折就拆成两句短叙，或直接写动作与结果。",
        "**破折号**：用「——」拖尾补充、叠比喻或改述的句子全章最多 3 句；禁止「事实句——体感比喻——再换说法强调」三连灌水。",
        "**信息**：大约每 300～500 字至少出现一条可复述的新事实（人物立场、线索、因果、决策、关系变化之一），禁止只换手改比喻形容同一件事。",
        "**情节密度（段落级）**：以空行分段为准，每一段内须至少含一项——具体动作并带轻微后果、有信息量的对白、发现/误会/决定之一落地、或可见的空间位移/关系试探；禁止连续两段只有景物/气味/光色铺陈而无人物动作、对白或取舍。",
        "**视角**：POV 体感描写少而准即可；同一情绪禁止用两条以上不同比喻接力渲染；设定交代优先对白、动作或一句平叙，忌长串内心排比。",
        "**扩写**：需要加长时，加有信息的对话轮次、可见动作链、带后果的选择；禁止复制粘贴式「众人震惊」排比，或重复描写同一条「电流/暖流/针刺」体感。",
    ]
    if beat_mode and beat_target_words is not None and beat_target_words <= 900:
        lines.extend(
            [
                "",
                "**节拍**：本段目标字数偏紧，优先完成大纲里的动作与关键对白；纠正式排比与内心铺陈从简。",
            ]
        )
    return "\n".join(lines)
